    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String Cname=jC1.getSelectedItem().toString();
        String sem1=txt1.getText();
        String sem2=txt2.getText();
        String sem3=txt3.getText();
        String sem4=txt4.getText();
        String sem5=txt5.getText();
        String sem6=txt6.getText();      
        if(txt0.getText().isEmpty() || txt1.getText().isEmpty() || txt2.getText().isEmpty() || txt4.getText().isEmpty() || txt3.getText().isEmpty() || txt5.getText().isEmpty() || txt6.getText().isEmpty()){          
           JOptionPane.showMessageDialog(this,"Please fill all details!");
       }else{ 
            int noofsem=Integer.parseInt(txt0.getText());
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/result","root","root");
            PreparedStatement ps1=con.prepareCall("select CourseName from Semister");
            ResultSet rs=ps1.executeQuery();
            while(rs.next()){
                String course=rs.getString("CourseName");
                if(course.equals(Cname)){
                    throw new myException("Duplicate Entry!");
                }
            }
            PreparedStatement ps=con.prepareStatement("insert into semister(CourseName,noOfSem,semister1,semister2,semister3,semister4,semister5,semister6)values(?,?,?,?,?,?,?,?)");
            ps.setString(1, Cname);
            ps.setInt(2, noofsem);
            ps.setString(3, sem1);
            ps.setString(4, sem2);
            ps.setString(5, sem3);
            ps.setString(6, sem4);
            ps.setString(7, sem5);
            ps.setString(8, sem6);            
            //ps.executeUpdate();
            if(ps.executeUpdate()==1){
                JOptionPane.showMessageDialog(this,"Semisters Added Successfully!");
                 txt0.setText("");
                 txt1.setText("");
                 txt2.setText("");
                 txt3.setText("");
                 txt4.setText("");
                 txt5.setText("");
                 txt6.setText("");
            }else{
                JOptionPane.showMessageDialog(this,"Unble to Add");
            }
            
        }catch(myException ex){
            JOptionPane.showMessageDialog(this,ex.getMessage());  
       }catch(Exception e){
            System.out.println("Error:"+e.getMessage());           
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

