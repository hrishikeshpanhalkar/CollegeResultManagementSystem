    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String CName=txt1.getText();
        String faculty=jC1.getSelectedItem().toString();
        String Degree=jC2.getSelectedItem().toString();
        String CType=jC3.getSelectedItem().toString();
        String CPattern=jC4.getSelectedItem().toString();
        String Fpattern=jC5.getSelectedItem().toString();
        
        //int i=busDoa.update(bus,movement,sourcecb,dest,date,time,price,seat,id);
        if(txt2.getText().isEmpty() || txt3.getText().isEmpty() || txt1.getText().isEmpty() || txt4.getText().isEmpty()){          
           JOptionPane.showMessageDialog(this,"Please Choose table row!");
       }else{
            int Duration=Integer.parseInt(txt2.getText()); 
            int Cno=Integer.parseInt(txt3.getText());
            int seat=Integer.parseInt(txt4.getText());
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/result","root","root");           
            PreparedStatement ps=con.prepareStatement("update Course Set CourseName=?,Faculty=?,Degree=?,CollegeType=?,Duration=?,CoursePattern=?,FeePattern=?,seat=? where CourseNo=?");
            ps.setString(1, CName);
            ps.setString(2, faculty);
            ps.setString(3, Degree);
            ps.setString(4, CType);
            ps.setInt(5,Duration );
            ps.setString(6, CPattern);
            ps.setString(7, Fpattern);
            ps.setInt(8, seat);
            ps.setInt(9,Cno );
            //ps.executeUpdate();
            if(ps.executeUpdate()==1){
                JOptionPane.showMessageDialog(this,"Update successfully!");
                jtableab();
            }else{
                JOptionPane.showMessageDialog(this,"Unble to update!");
            }
            ps.close();
            
        }catch(Exception e){
            System.out.println("Error:"+e.getMessage());
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

