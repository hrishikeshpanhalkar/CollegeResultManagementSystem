        Connection conn=null;
        try{ 
            Class.forName("com.mysql.jdbc.Driver");
            conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/result","root","root");
            int rows=jTable1.getRowCount();
            conn.setAutoCommit(false);
            int External=0;
            int internal=0;
            int total=0;
            PreparedStatement pst=conn.prepareStatement("insert into result(Roll_no,StudentName,SubCode,CourseName,Semister,SubjectName,Credits,External,Externalgrade,Internal,InternalGrade,Total,TotalGrade,Gradepoints,CG) values(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
            for(int row=0;row<rows;row++){
                int roll=Integer.parseInt((String) jTable1.getValueAt(row, 0));
                String Name=(String) jTable1.getValueAt(row, 1);
                External=Integer.parseInt((String) jTable1.getValueAt(row, 2));
                internal=Integer.parseInt((String) jTable1.getValueAt(row, 3));
                int ac=Integer.parseInt((String) jTable1.getValueAt(row, 4));
                total=(External + internal);
                if((External>ex) || (internal>in) || (total>to)){
                    throw new myException("enter marks greater the total marks!");
                }
                int Egrade=(int)((External*100.00)/ex);                
                String externalgrade=null; 
                
               if(Egrade > 79){
                    externalgrade="O";   
                    
                }else if((Egrade<80) && (Egrade>69)){
                    externalgrade="A+";
                  
                }else if(Egrade<70&& Egrade>59){
                    externalgrade="A";
                  
                }else if(Egrade<60 && Egrade>54){
                    externalgrade="B+";
                 
                }else if(Egrade<55 && Egrade>49){
                    externalgrade="B";
                   
                }else if(Egrade<50 && Egrade>44){
                    externalgrade="C";
                    
                }else if(Egrade<45 && Egrade>39){
                    externalgrade="D";
                 
                }else if(Egrade<40){
                    
                    externalgrade="F";                  
               }
                
                int igrade=(int)((internal*100.00)/in); 
                
                String internalgrade=null; 
                if(igrade > 79){
                    internalgrade="O";   
                    
                }else if((igrade<80) && (igrade>69)){
                    internalgrade="A+";
                  
                }else if(igrade<70&& igrade>59){
                    internalgrade="A";
                  
                }else if(igrade<60 && igrade>54){
                    internalgrade="B+";
                 
                }else if(igrade<55 && igrade>49){
                    internalgrade="B";
                   
                }else if(igrade<50 && igrade>44){
                    internalgrade="C";
                    
                }else if(igrade<45 && igrade>39){
                    internalgrade="D";
                 
                }else if(igrade<40){
                    
                    internalgrade="F";                  
               }
                int gradepoint=0;
                int grade=(int)((total*100.00)/to); 
                
                String totalgrade=null; 
               
                if(grade > 79){
                    totalgrade="O";   
                    gradepoint=10;
                }else if((grade<80) && (grade>69)){
                    totalgrade="A+";
                    gradepoint=9;
                }else if(grade<70&& grade>59){
                    totalgrade="A";
                    gradepoint=8;
                }else if(grade<60 && grade>54){
                    totalgrade="B+";
                    gradepoint=7;
                }else if(grade<55 && grade>49){
                    totalgrade="B";
                    gradepoint=6;
                }else if(grade<50 && grade>44){
                    totalgrade="C";
                    gradepoint=5;
                }else if(grade<45 && grade>39){
                    totalgrade="D";
                    gradepoint=4;
                }else if(grade<40){
                    
                    totalgrade="F";   
                    gradepoint=0;
               }
                int CG=(credit*gradepoint);  
                if(in==0){
                     internalgrade="--";
                }
                
                    pst.setInt(1, roll);
                    pst.setString(2, Name);
                    pst.setString(3, sCode);
                    pst.setString(4,cname);
                    pst.setString(5,sem);
                    pst.setString(6,sub);
                    pst.setInt(7, credit);
                    pst.setInt(8, External);
                    pst.setString(9, externalgrade);
                    pst.setDouble(10, internal);
                    pst.setString(11, internalgrade);
                    pst.setInt(12, total);
                    pst.setString(13, totalgrade);
                    pst.setInt(14, gradepoint);
                    pst.setInt(15, CG);  
                    pst.addBatch();                   
            }  
            int[] ab=pst.executeBatch();
            conn.commit();
          JOptionPane.showMessageDialog(this,"Mark Entry added successfully!");                   
       }catch(SQLIntegrityConstraintViolationException e){
           JOptionPane.showMessageDialog(this,"mark already entered!");         
       }catch(myException ex){
            try {
                conn.rollback();
                pst.clearBatch();
            } catch (SQLException ex1) {
                Logger.getLogger(ENTRY_TABLE.class.getName()).log(Level.SEVERE, null, ex1);
            }
            JOptionPane.showMessageDialog(this,ex.getMessage());  
       }
        catch(Exception e){
            System.out.println(e);
       }
