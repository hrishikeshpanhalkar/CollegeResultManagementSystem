    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String uname=txt1.getText();
        String Opass=String.valueOf(p1.getPassword());
        String Npass=String.valueOf(p2.getPassword());
        boolean i=ResultDoa.ChangePass(uname,Opass,Npass);
        if(uname.equals("") && Opass.equals("") && Npass.equals("")){
            JOptionPane.showMessageDialog(this,"please fill all content!");
        }
        else{
            if(i==true)
            {
                JOptionPane.showMessageDialog(this,"Password Successfully updated");
            }else
            {
                JOptionPane.showMessageDialog(this,"Please enter valid username & password");
                txt1.setText("");
                p1.setText("");
                p2.setText("");
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

