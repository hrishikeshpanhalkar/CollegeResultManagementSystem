    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String cname=jC1.getSelectedItem().toString();
        String semister=jC2.getSelectedItem().toString();
        String sub=txt1.getText();
        String scode=txt2.getText();
        if(sub.isEmpty()|| scode.isEmpty()){
            JOptionPane.showMessageDialog(this,"Fill details");
        }else{
        try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/result","root","root");
                PreparedStatement ps = con.prepareStatement("insert into allsubject values(?,?,?,?)");
                ps.setString(1, cname);
                ps.setString(2, semister);
                ps.setString(3, scode);
                ps.setString(4, sub);
               if(ps.executeUpdate()==1){
                   JOptionPane.showMessageDialog(this,"Subject Added Successfully!");
               }
                ps.close();
            } catch(SQLIntegrityConstraintViolationException e){
            JOptionPane.showMessageDialog(this,"Duplicate Entry!");          
        }catch(Exception e) {
                JOptionPane.showMessageDialog(this,"Error:"+e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

