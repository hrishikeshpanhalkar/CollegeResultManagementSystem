    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String Cname=txt1.getText();
        String Faculty=jC1.getSelectedItem().toString();
        String Degree=jC2.getSelectedItem().toString();
        String Ctype=jC3.getSelectedItem().toString();
        String Cpattern=jC4.getSelectedItem().toString();
        String Fpattern=jC5.getSelectedItem().toString();
        if(txt1.getText().isEmpty() || txt2.getText().isEmpty() || txt3.getText().isEmpty()){          
           JOptionPane.showMessageDialog(this,"Please fill all details!");
       }else{ 
            int Duration=Integer.parseInt(txt2.getText());
            int Seat=Integer.parseInt(txt3.getText());
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/result","root","root");
            PreparedStatement ps1=con.prepareCall("select CourseName from course");
            ResultSet rs=ps1.executeQuery();
            while(rs.next()){
                String course=rs.getString("CourseName");
                if(course.equals(Cname)){
                    throw new myException("Duplicate Entry!");
                }
            }
            PreparedStatement ps=con.prepareStatement("insert into course(CourseName,Faculty,Degree,CollegeType,Duration,CoursePattern,FeePattern,seat)values(?,?,?,?,?,?,?,?)");
            ps.setString(1, Cname);
            ps.setString(2, Faculty);
            ps.setString(3, Degree);
            ps.setString(4, Ctype);
            ps.setInt(5, Duration);
            ps.setString(6, Cpattern);
            ps.setString(7, Fpattern);
            ps.setInt(8, Seat);
            if(ps.executeUpdate()==1){
                JOptionPane.showMessageDialog(this,"Course Added Successfully!");
                 txt1.setText("");
                 txt2.setText("");
                 txt3.setText("");
            }else{
                JOptionPane.showMessageDialog(this,"Unble to Add");
            }
        }catch(myException ex){
            JOptionPane.showMessageDialog(this,ex.getMessage());  
       }
        catch(Exception e){
            System.out.println("Error:"+e.getMessage());           
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

