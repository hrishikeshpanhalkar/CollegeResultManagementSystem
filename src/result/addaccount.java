    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String uname=txt1.getText();
        String password=String.valueOf(p1.getPassword());
        String Atype=jC1.getSelectedItem().toString();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/result","root","root");
            PreparedStatement ps1 = con.prepareStatement("Insert into user values(?,?,?)");
            ps1.setString(1,uname);
            ps1.setString(2,password);
            ps1.setString(3,Atype);
            if(ps1.executeUpdate()==1){
                JOptionPane.showMessageDialog(this, "User Added Successfully!");
            }
        }
        catch(SQLIntegrityConstraintViolationException e){
            JOptionPane.showMessageDialog(this,"Username Already used!");
        }catch(Exception e1){
            System.out.println(e1);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

